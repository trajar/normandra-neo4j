package org.normandra.neo4j;

import org.apache.commons.lang.NullArgumentException;
import org.apache.commons.lang.StringUtils;
import org.neo4j.graphdb.Relationship;
import org.neo4j.graphdb.RelationshipType;
import org.normandra.NormandraException;
import org.normandra.Transaction;
import org.normandra.data.DataHandler;
import org.normandra.data.EntityReference;
import org.normandra.data.GraphDataHandler;
import org.normandra.data.StaticEntityReference;
import org.normandra.graph.Edge;
import org.normandra.graph.GraphEntitySession;
import org.normandra.graph.Node;
import org.normandra.graph.NodeQuery;
import org.normandra.meta.EntityMeta;
import org.normandra.property.PropertyModel;
import org.normandra.util.ArraySet;
import org.normandra.util.EntityPersistence;

import java.util.*;
import java.util.stream.Collectors;

/**
 * wrapped neo4j node context
 * <p>
 * Date: 7/12/14
 */
public class Neo4jNode<T> implements Node<T> {
    private final Neo4jGraph graph;

    private final org.neo4j.graphdb.Node node;

    private final EntityMeta meta;

    private EntityReference<T> data;

    public Neo4jNode(final Neo4jGraph graph, final org.neo4j.graphdb.Node node, final EntityMeta meta, final EntityReference<T> ref) {
        if (null == graph) {
            throw new NullArgumentException("graph");
        }
        if (null == node) {
            throw new NullArgumentException("node");
        }
        if (null == ref) {
            throw new NullArgumentException("reference");
        }
        if (null == meta) {
            throw new NullArgumentException("meta");
        }
        this.graph = graph;
        this.node = node;
        this.meta = meta;
        this.data = ref;
    }

    public final org.neo4j.graphdb.Node api() {
        return this.node;
    }

    @Override
    public T getEntity() throws NormandraException {
        return this.data.getInstance();
    }

    @Override
    public void delete() throws NormandraException {
        try (final Transaction tx = this.graph.beginTransaction()) {
            this.api().delete();
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to remove node [" + this.node + "].", e);
        }
    }

    @Override
    public <E, N> boolean hasEdge(final Node<N> node, final E entity) throws NormandraException {
        if (null == node) {
            throw new NullArgumentException("node");
        }
        if (null == entity) {
            return false;
        }
        for (final Edge<?> edge : this.getEdgesByTypes(entity.getClass())) {
            if (entity.equals(edge.getEntity()) && node.equals(edge.getOtherNode(this))) {
                return true;
            }
        }
        return false;
    }

    @Override
    public <E, N> Neo4jEdge<E> createEdge(final Node<N> node, final E entity) throws NormandraException {
        if (null == node) {
            throw new NullArgumentException("node");
        }

        final Neo4jNode neo4j = (Neo4jNode) node;
        final Class<?> clazz = entity.getClass();
        final EntityMeta meta = this.graph.getMeta().getEdgeMeta(clazz);
        if (null == meta) {
            return null;
        }
        if (!this.graph.getMeta().isEdgeEntity(meta)) {
            throw new IllegalArgumentException("Entity [" + meta + "] is not a registered edge type.");
        }

        final RelationshipType type = Neo4jUtils.getRelationshipType(meta);
        try (final Transaction tx = this.graph.beginTransaction()) {
            // save relationship properties
            final Relationship relationship = this.node.createRelationshipTo(neo4j.node, type);
            if (null == relationship) {
                return null;
            }
            final PropertyModel model = this.graph.buildModel(meta, relationship);
            final DataHandler handler = new GraphDataHandler(model);
            new EntityPersistence(new GraphEntitySession(this.graph)).save(meta, entity, handler);

            // cache instance
            final Object key = meta.getId().fromEntity(entity);
            final EntityReference<E> ref = new StaticEntityReference<>(entity);
            final Neo4jEdge edge = new Neo4jEdge(this.graph, relationship, ref);
            this.graph.getCache().put(meta, key, edge);

            // done
            tx.success();
            return edge;
        } catch (final Exception e) {
            throw new NormandraException("Unable to create edge from [" + this + "] -> [" + node + "].", e);
        }
    }

    @Override
    public void updateEntity(final T entity) throws NormandraException {
        // get entity context
        final Class<?> clazz = entity.getClass();
        final EntityMeta meta = this.graph.getMeta().getNodeMeta(clazz);
        if (null == meta) {
            throw new IllegalArgumentException();
        }

        try (final Transaction tx = this.graph.beginTransaction()) {
            // save node
            final PropertyModel model = this.graph.buildModel(meta, this.api());
            final GraphDataHandler handler = new GraphDataHandler(model);
            new EntityPersistence(new GraphEntitySession(this.graph)).save(meta, entity, handler);
            tx.success();
        } catch (final Exception e) {
            throw new NormandraException("Unable to update node [" + this + "].", e);
        }

        // update instance
        this.data = new StaticEntityReference<>(entity);
    }

    @Override
    public int getDegree() throws NormandraException {
        try {
            try (final Transaction tx = this.graph.beginTransaction()) {
                return this.node.getDegree();
            }
        } catch (final Exception e) {
            throw new NormandraException("Unable to get node degree.", e);
        }
    }

    private Map<String, Object> parametersForMyself() {
        final Map<String, Object> params = new HashMap<>(1);
        params.put("id", this.node.getId());
        return params;
    }

    @Override
    public Iterable<Node> getNeighbors() throws NormandraException {
        final Set<Node> neighbors = new HashSet<>();
        try {
            try (final NodeQuery<?> q = this.graph.queryNodes(
                    null, // node could be any type
                    "MATCH (n)--(connected) WHERE id(n)=$id RETURN DISTINCT connected",
                    parametersForMyself())) {
                for (final Node node : q) {
                    if (!node.equals(this)) {
                        neighbors.add(node);
                    }
                }
            }
        } catch (Exception e) {
            throw new NormandraException("Unable to query neighbors.", e);
        }
        return Collections.unmodifiableCollection(neighbors);
    }

    @Override
    public <E, N> Iterable<Node<N>> getNeighbors(Class<E> edgeType, Class<N> nodeType) throws NormandraException {
        final EntityMeta nodeMeta = nodeType != null ? this.graph.getMeta().getNodeMeta(nodeType) : null;
        final EntityMeta edgeMeta = edgeType != null ? this.graph.getMeta().getEdgeMeta(edgeType) : null;

        String edgeLabel = "";
        if (edgeMeta != null) {
            edgeLabel = "[:" + Neo4jUtils.getRelationshipType(edgeMeta).name() + "]";
        }

        final Set<Node<N>> neighbors = new HashSet<>();
        try {
            try (final NodeQuery<N> q = this.graph.queryNodes(
                    nodeMeta,
                    "MATCH (n)-" + edgeLabel + "-(connected) WHERE id(n)=$id RETURN DISTINCT connected",
                    parametersForMyself())) {
                for (final Node<N> node : q) {
                    if (!node.equals(this)) {
                        neighbors.add(node);
                    }
                }
            }
        } catch (Exception e) {
            throw new NormandraException("Unable to query neighbors.", e);
        }
        return Collections.unmodifiableCollection(neighbors);
    }

    @Override
    public Iterable<Node> expand(final int depth) throws NormandraException {
        final Set<Node> neighbors = new HashSet<>();
        try {
            try (final NodeQuery<?> q = this.graph.queryNodes(
                    null, // node could be any type
                    "MATCH (n)-[*1.." + depth + "]-(connected) WHERE id(n)=$id RETURN DISTINCT connected",
                    parametersForMyself())) {
                for (final Node node : q) {
                    if (!node.equals(this)) {
                        neighbors.add(node);
                    }
                }
            }
        } catch (Exception e) {
            throw new NormandraException("Unable to query neighbors.", e);
        }
        return Collections.unmodifiableCollection(neighbors);
    }

    @Override
    public <E, N> Iterable<Node<N>> expandByType(int depth, Class<E> edgeType) throws NormandraException {
        return this.expandByTypes(depth, Collections.singleton(edgeType));
    }

    @Override
    public <E, N> Iterable<Node<N>> expandByTypes(final int depth, final Iterable<Class<E>> edgeTypes) throws NormandraException {

        final Set<String> edgeLabels = new ArraySet<>();
        if (edgeTypes != null) {
            for (final Class<?> clazz : edgeTypes) {
                final EntityMeta meta = this.graph.getMeta().getEdgeMeta(clazz);
                if (meta != null) {
                    edgeLabels.add(Neo4jUtils.getRelationshipType(meta).name());
                }
            }
        }
        final String edgeLabel = StringUtils.join(
                edgeLabels.stream().map(x -> ":" + x).collect(Collectors.toList()),
                "|");

        final Set<Node<N>> neighbors = new HashSet<>();
        try {
            try (final NodeQuery<?> q = this.graph.queryNodes(
                    null, // node could be any type
                    "MATCH (n)-[" + edgeLabel + "*1.." + depth + "]-(connected) WHERE id(n)=$id RETURN DISTINCT connected",
                    parametersForMyself())) {
                for (final Node node : q) {
                    if (!node.equals(this)) {
                        neighbors.add(node);
                    }
                }
            }
        } catch (Exception e) {
            throw new NormandraException("Unable to query neighbors.", e);
        }
        return Collections.unmodifiableCollection(neighbors);
    }

    @Override
    public Collection<Edge> getEdges() throws NormandraException {
        try (final org.normandra.Transaction tx = this.graph.beginTransaction()) {
            final int num = this.node.getDegree();
            final List<Edge> edges = new ArrayList<>(num);
            for (final Relationship relationship : this.node.getRelationships()) {
                final Edge edge = this.graph.buildEdge(relationship);
                if (edge != null) {
                    edges.add(edge);
                }
            }
            return Collections.unmodifiableList(edges);
        } catch (final Exception e) {
            throw new NormandraException("Unable to create edge from [" + this + "] -> [" + node + "].", e);
        }
    }

    @Override
    public Collection<Edge> getEdgesByType(final Class clazz) throws NormandraException {
        final EntityMeta meta = this.graph.getMeta().getEdgeMeta(clazz);
        if (null == meta) {
            return null;
        }

        try (final org.normandra.Transaction tx = this.graph.beginTransaction()) {
            final Set<Edge> edges = new HashSet<>();
            final RelationshipType type = Neo4jUtils.getRelationshipType(meta);
            for (final Relationship relationship : this.node.getRelationships(type)) {
                final Edge edge = this.graph.buildEdge(relationship);
                if (edge != null) {
                    edges.add(edge);
                }
            }
            return Collections.unmodifiableCollection(edges);
        } catch (final Exception e) {
            throw new NormandraException("Unable to create edge from [" + this + "] -> [" + node + "].", e);
        }
    }

    @Override
    public <E> Collection<Edge<E>> getEdgesByTypes(final Iterable<Class<E>> types) throws NormandraException {
        if (null == types) {
            return Collections.emptyList();
        }

        // lookup relationship types
        final Set<RelationshipType> relations = new HashSet<>();
        for (final Class<?> clazz : types) {
            final EntityMeta meta = this.graph.getMeta().getEdgeMeta(clazz);
            if (meta != null) {
                final RelationshipType type = Neo4jUtils.getRelationshipType(meta);
                if (type != null) {
                    relations.add(type);
                }
            }
        }
        if (relations.isEmpty()) {
            return Collections.emptyList();
        }

        // query edges
        try (final org.normandra.Transaction tx = this.graph.beginTransaction()) {
            final int num = this.node.getDegree();
            final List<Edge<E>> edges = new ArrayList<>(num);
            final RelationshipType[] list = relations.toArray(new RelationshipType[relations.size()]);
            for (final Relationship relationship : this.node.getRelationships(list)) {
                final Edge edge = this.graph.buildEdge(relationship);
                if (edge != null) {
                    edges.add(edge);
                }
            }
            return Collections.unmodifiableList(edges);
        } catch (final Exception e) {
            throw new NormandraException("Unable to create edge from [" + this + "] -> [" + node + "].", e);
        }
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Neo4jNode<?> neo4jNode = (Neo4jNode<?>) o;
        return Objects.equals(graph, neo4jNode.graph) &&
                Objects.equals(node, neo4jNode.node);
    }

    @Override
    public int hashCode() {
        return Objects.hash(graph, node);
    }
}
